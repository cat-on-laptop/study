import sys
si = sys.stdin.readline

s = si().rstrip()

arr = set()

for i in range(len(s)):
    temp = s[i]
    arr.add(temp)
    for j in range(i + 1, len(s)):
        temp += s[j]
        arr.add(temp)


print(len(arr))
